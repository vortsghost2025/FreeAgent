#!/usr/bin/env node
/**
 * AUTOMATED TASK ASSIGNMENT SERVICE
 * Continuously monitors and automatically assigns tasks to agents
 */

import fs from 'fs';
import path from 'path';
import { TaskAssignmentSystem } from './agent-task-manager.js';

class AutomatedTaskAssignmentService {
  constructor() {
    this.taskSystem = new TaskAssignmentSystem();
    this.monitoringInterval = null;
    this.autoAssignmentInterval = null;
    this.isRunning = false;
    
    // Task queues that get automatically processed
    this.pendingTasks = [];
    this.recurringTasks = [];
  }

  async start() {
    if (this.isRunning) {
      console.log('⚠️  Service already running');
      return;
    }

    console.log('🚀 STARTING AUTOMATED TASK ASSIGNMENT SERVICE');
    console.log('==============================================');
    
    await this.taskSystem.initialize();
    this.isRunning = true;
    
    // Start monitoring intervals
    this.startMonitoring();
    this.startAutoAssignment();
    this.loadRecurringTasks();
    
    console.log('✅ Automated task assignment service is now running');
    console.log('📋 Monitoring for new tasks every 5 seconds');
    console.log('🔄 Auto-assigning pending tasks every 30 seconds');
    console.log('⌨️  Press Ctrl+C to stop the service\n');
  }

  stop() {
    if (!this.isRunning) return;
    
    console.log('\n🛑 STOPPING AUTOMATED TASK ASSIGNMENT SERVICE');
    
    if (this.monitoringInterval) {
      clearInterval(this.monitoringInterval);
    }
    if (this.autoAssignmentInterval) {
      clearInterval(this.autoAssignmentInterval);
    }
    
    this.isRunning = false;
    console.log('✅ Service stopped');
  }

  startMonitoring() {
    // Monitor various sources for new tasks
    this.monitoringInterval = setInterval(async () => {
      await this.checkForNewTasks();
    }, 5000); // Check every 5 seconds
  }

  startAutoAssignment() {
    // Automatically assign pending tasks
    this.autoAssignmentInterval = setInterval(async () => {
      await this.processPendingTasks();
    }, 30000); // Process every 30 seconds
  }

  async checkForNewTasks() {
    try {
      // Check file-based task queue
      const taskQueueFile = 'auto-task-queue.json';
      if (fs.existsSync(taskQueueFile)) {
        const queueData = JSON.parse(await fs.promises.readFile(taskQueueFile, 'utf8'));
        if (queueData.tasks && queueData.tasks.length > 0) {
          for (const task of queueData.tasks) {
            if (!this.pendingTasks.some(t => t.id === task.id)) {
              this.pendingTasks.push(task);
              console.log(`📥 New task detected: ${task.description}`);
            }
          }
          // Clear the queue file
          await fs.promises.writeFile(taskQueueFile, JSON.stringify({ tasks: [] }, null, 2));
        }
      }

      // Check for system-generated tasks
      await this.checkSystemTasks();

    } catch (error) {
      console.error('❌ Error checking for tasks:', error.message);
    }
  }

  async checkSystemTasks() {
    // Generate maintenance tasks based on system state
    const systemStatus = await this.getSystemStatus();
    
    // Auto-generate tasks based on system needs
    if (systemStatus.memoryUtilization > 80) {
      this.addSystemTask({
        description: "Optimize memory usage and clean buffers",
        requiredSkills: ["debugging", "memory", "optimization"],
        priority: "high",
        source: "system_monitor"
      });
    }

    if (systemStatus.idleAgents.length > 0) {
      this.addSystemTask({
        description: "Perform routine system diagnostics",
        requiredSkills: ["testing", "analysis", "diagnostics"],
        priority: "medium",
        source: "system_monitor"
      });
    }

    // Periodic cleanup tasks
    if (new Date().getMinutes() === 0) { // Hourly
      this.addSystemTask({
        description: "Hourly system health check and log rotation",
        requiredSkills: ["maintenance", "logging", "monitoring"],
        priority: "low",
        source: "scheduled"
      });
    }
  }

  addSystemTask(taskConfig) {
    const taskId = `auto-${Date.now()}-${Math.random().toString(36).substr(2, 9)}`;
    const task = {
      id: taskId,
      ...taskConfig,
      createdAt: new Date().toISOString(),
      autoGenerated: true
    };

    if (!this.pendingTasks.some(t => t.id === taskId)) {
      this.pendingTasks.push(task);
      console.log(`🤖 Auto-generated task: ${task.description} (${task.priority})`);
    }
  }

  async processPendingTasks() {
    if (this.pendingTasks.length === 0) return;

    console.log(`\n🔄 Processing ${this.pendingTasks.length} pending tasks...`);
    
    const tasksToProcess = [...this.pendingTasks];
    this.pendingTasks = [];

    for (const task of tasksToProcess) {
      try {
        const assignedAgent = this.taskSystem.findBestAgent(task.requiredSkills || []);
        
        if (assignedAgent) {
          await this.taskSystem.assignTask(task.description, task.requiredSkills || []);
          console.log(`✅ Assigned: ${task.description} → ${this.taskSystem.agents[assignedAgent].name}`);
        } else {
          // Re-queue if no agent available
          this.pendingTasks.push(task);
          console.log(`⏸️  Queued: ${task.description} (no suitable agent)`);
        }
      } catch (error) {
        console.error(`❌ Failed to process task ${task.id}:`, error.message);
        this.pendingTasks.push(task); // Retry later
      }
    }
  }

  loadRecurringTasks() {
    // Load recurring maintenance tasks
    this.recurringTasks = [
      {
        schedule: 'daily',
        time: '02:00',
        task: {
          description: "Daily system backup and data validation",
          requiredSkills: ["devops", "database", "backup"],
          priority: "high"
        }
      },
      {
        schedule: 'weekly',
        day: 'Sunday',
        time: '03:00',
        task: {
          description: "Weekly performance analysis and optimization",
          requiredSkills: ["data", "analysis", "optimization"],
          priority: "medium"
        }
      }
    ];

    // Schedule recurring tasks
    setInterval(() => {
      this.checkRecurringTasks();
    }, 60000); // Check every minute
  }

  checkRecurringTasks() {
    const now = new Date();
    const currentTime = `${now.getHours().toString().padStart(2, '0')}:${now.getMinutes().toString().padStart(2, '0')}`;
    const currentDay = now.toLocaleDateString('en-US', { weekday: 'long' });

    for (const recurring of this.recurringTasks) {
      if (recurring.schedule === 'daily' && recurring.time === currentTime) {
        this.addSystemTask({
          ...recurring.task,
          source: "scheduled_recurring"
        });
      }
      
      if (recurring.schedule === 'weekly' && 
          recurring.day === currentDay && 
          recurring.time === currentTime) {
        this.addSystemTask({
          ...recurring.task,
          source: "scheduled_recurring"
        });
      }
    }
  }

  async getSystemStatus() {
    // Simulate getting system status
    // In real implementation, this would query actual system metrics
    return {
      memoryUtilization: Math.floor(Math.random() * 100),
      idleAgents: ['code', 'test'].filter(() => Math.random() > 0.5),
      totalAgents: 9,
      activeTasks: Math.floor(Math.random() * 5)
    };
  }

  // API Methods for external control
  async addTask(description, skills = [], priority = 'medium') {
    const task = {
      id: `api-${Date.now()}-${Math.random().toString(36).substr(2, 9)}`,
      description,
      requiredSkills: skills,
      priority,
      createdAt: new Date().toISOString(),
      source: "api"
    };
    
    this.pendingTasks.push(task);
    console.log(`📥 API task added: ${description}`);
    return task.id;
  }

  getPendingTasks() {
    return this.pendingTasks;
  }

  getAssignedTasks() {
    return this.taskSystem.tasks.filter(t => t.status === 'assigned');
  }

  async getAgentWorkload() {
    const workload = {};
    for (const [agentKey, agent] of Object.entries(this.taskSystem.agents)) {
      workload[agentKey] = {
        name: agent.name,
        status: agent.status,
        assignedTasks: this.taskSystem.tasks.filter(t => 
          t.assignedTo === agentKey && t.status === 'assigned'
        ).length
      };
    }
    return workload;
  }
}

// Handle graceful shutdown
process.on('SIGINT', () => {
  console.log('\n_received SIGINT, shutting down gracefully...');
  if (global.automatedService) {
    global.automatedService.stop();
  }
  process.exit(0);
});

// Command line interface
async function main() {
  const args = process.argv.slice(2);
  const service = new AutomatedTaskAssignmentService();
  global.automatedService = service; // Make available for shutdown

  switch (args[0]) {
    case 'start':
      await service.start();
      break;
      
    case 'status':
      console.log('📊 AUTOMATED SERVICE STATUS:');
      console.log(`Running: ${service.isRunning ? '✅ Yes' : '❌ No'}`);
      console.log(`Pending Tasks: ${service.pendingTasks.length}`);
      console.log(`Assigned Tasks: ${service.getAssignedTasks().length}`);
      break;
      
    case 'add':
      if (args.length >= 3) {
        const description = args.slice(2).join(' ');
        const skills = args[1].split(',');
        await service.addTask(description, skills);
      } else {
        console.log('Usage: node automated-task-service.js add skill1,skill2 "Task description"');
      }
      break;
      
    default:
      console.log('🤖 AUTOMATED TASK ASSIGNMENT SERVICE');
      console.log('====================================');
      console.log('Commands:');
      console.log('  start          - Start the automated service');
      console.log('  status         - Show current service status');
      console.log('  add skills "task" - Add a task via command line');
      console.log('\nExample:');
      console.log('  node automated-task-service.js start');
      console.log('  node automated-task-service.js add coding,api "Fix authentication bug"');
  }
}

// Run if called directly
if (import.meta.url === `file://${process.argv[1]}`) {
  main().catch(console.error);
}

export { AutomatedTaskAssignmentService };